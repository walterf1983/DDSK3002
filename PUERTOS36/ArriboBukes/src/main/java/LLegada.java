
public class LLegada {
	
	private int fecha;
	private int hora;
	
	public int getFecha() {
		return fecha;
	}
	public int getHora() {
		return hora;
	}
	
	public LLegada(int fecha, int hora) {
		super();
		this.fecha = fecha;
		this.hora = hora;
	}
	public void setFecha(int fecha) {
		this.fecha = fecha;
	}
	public void setHora(int hora) {
		this.hora = hora;
	}
	
	
}
